package com.company;

public class Expoente {
    //Esse metodo retorna o quadradado do numero
    public static double elevar(double a){
        return a * a;
    }
    
    //Esse metodo retorna um numero elevado o outro
    public static  double elevar(double a, double b ){
        return Math.pow(a,b);
    }
    
    
}
